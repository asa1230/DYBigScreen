package com.dayang.newmedia.newscommand.ws.client.creclient.schema;

public class UserLoadModel {
	private String id;
	private String name;
	private String workNo;
	private String organizationId;
	private String password;
	private String description;
	private String status;
	private String tel;
	private String mail;
	private int type;
	private int personalSpaceFlag;
	private long maxCapacity;
	private String iconUrl;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getWorkNo() {
		return workNo;
	}
	public void setWorkNo(String workNo) {
		this.workNo = workNo;
	}
	public String getOrganizationId() {
		return organizationId;
	}
	public void setOrganizationId(String organizationId) {
		this.organizationId = organizationId;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getPersonalSpaceFlag() {
		return personalSpaceFlag;
	}
	public void setPersonalSpaceFlag(int personalSpaceFlag) {
		this.personalSpaceFlag = personalSpaceFlag;
	}
	public long getMaxCapacity() {
		return maxCapacity;
	}
	public void setMaxCapacity(long maxCapacity) {
		this.maxCapacity = maxCapacity;
	}
	public String getIconUrl() {
		return iconUrl;
	}
	public void setIconUrl(String iconUrl) {
		this.iconUrl = iconUrl;
	}

}
